public class java_operator {
    public static void main(String[]args){
        int a=40;
        int b=10;
        //Arthimatic ope
        System.out.println(a%b);
        //Relational/comparission ope
        System.out.println(b>a);
        System.out.println(a!=b);
        //logical ope
        System.out.println(a>b&&b>a);
        System.out.println(a>b||b>a);
        //Assignment ope
        b+=3;//b=b+3
        System.out.println(b);
        //increment/decrement
        a++;
        System.out.println(a);

    }
}
