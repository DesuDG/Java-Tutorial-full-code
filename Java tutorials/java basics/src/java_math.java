public class java_math {
    public static void main(String[] args) {
        System.out.println(Math.min(23,34));//minimum
        System.out.println(Math.max(23,34));//maximum
        System.out.println(Math.abs(-123));//absolute value
        System.out.println(Math.sqrt(169));//square root
        System.out.println(Math.pow(6,2));//power
        System.out.println(Math.random());//random number
        int num=(int)(Math.random()*101);
        System.out.println(num);
    }
}
