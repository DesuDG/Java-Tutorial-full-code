public class static_poly {
//Method overloading
    public void add(int i, int j){
        System.out.println("i+j = "+(i+j));
    }
    public void add(int i, int j,int k){
        System.out.println("i+j+k = "+(i+j+k));
    }
    public void add(int i, long j){
        System.out.println("i+j = "+(i+j));
    }
}
